namespace Trilobit\BootstrapBundle\ContaoManager;

use Contao\ManagerPlugin\Bundle\Config\BundleConfig;
use Contao\ManagerPlugin\Bundle\BundlePluginInterface;
use Contao\ManagerPlugin\Bundle\Config\ModuleConfig;
use Contao\ManagerPlugin\Bundle\Parser\ParserInterface;

/**
 * Plugin for the Contao Manager.
 */
class Plugin implements BundlePluginInterface
{
    /**
     * {@inheritdoc}
     */
    public function getBundles(ParserInterface $parser)
    {
        return [
            BundleConfig::create('Trilobit\BootstrapBundle\TrilobitBootstrapBundle')
                ->setLoadAfter(['Contao\CoreBundle\ContaoCoreBundle', 'terminal42/contao-fineuploader'])
        ];
    }
}
